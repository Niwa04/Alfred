const { getDatabaseClient, normalizeDatabaseRows } = require('./database');

function normalizeDate(value) {
  if (value instanceof Date) {
    return value.toISOString().slice(0, 10);
  }

  return String(value || '').slice(0, 10).trim();
}

function normalizeTime(value) {
  return String(value || '').slice(0, 5).trim();
}

function normalizeShowDate(date) {
  return {
    id: String(date.id),
    date: normalizeDate(date.date || date.event_date),
    time: normalizeTime(date.time || date.event_time),
    title: String(date.title || '').trim(),
    venue: String(date.venue || '').trim(),
    reservationUrl: String(date.reservationUrl || date.reservation_url || '').trim()
  };
}

function validateShowDate(date) {
  const normalizedDate = normalizeShowDate(date);

  if (!normalizedDate.date || !normalizedDate.time || !normalizedDate.title || !normalizedDate.venue) {
    return { error: 'La date, l heure, le titre et le lieu sont obligatoires.' };
  }

  if (!/^\d{4}-\d{2}-\d{2}$/.test(normalizedDate.date)) {
    return { error: 'La date doit etre au format AAAA-MM-JJ.' };
  }

  if (!/^\d{2}:\d{2}$/.test(normalizedDate.time)) {
    return { error: 'L heure doit etre au format HH:MM.' };
  }

  if (normalizedDate.reservationUrl && !/^https?:\/\/\S+$/i.test(normalizedDate.reservationUrl)) {
    return { error: 'Le lien de reservation doit commencer par http:// ou https://.' };
  }

  return { date: normalizedDate };
}

async function ensureShowDatesTable(db) {
  await db.sql`
    CREATE TABLE IF NOT EXISTS show_dates (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      event_date DATE NOT NULL,
      event_time TIME NOT NULL,
      title TEXT NOT NULL,
      venue TEXT NOT NULL,
      reservation_url TEXT NOT NULL DEFAULT '',
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `;
}

async function getShowDates() {
  const db = await getDatabaseClient();
  await ensureShowDatesTable(db);

  const result = await db.sql`
    SELECT id, event_date, event_time, title, venue, reservation_url
    FROM show_dates
    ORDER BY event_date ASC, event_time ASC, id ASC
  `;

  return normalizeDatabaseRows(result).map(normalizeShowDate);
}

async function addShowDate(date) {
  const validation = validateShowDate(date);
  if (validation.error) {
    return validation;
  }

  const db = await getDatabaseClient();
  await ensureShowDatesTable(db);

  const result = await db.sql`
    INSERT INTO show_dates (event_date, event_time, title, venue, reservation_url)
    VALUES (${validation.date.date}::date, ${validation.date.time}::time, ${validation.date.title}, ${validation.date.venue}, ${validation.date.reservationUrl})
    RETURNING id, event_date, event_time, title, venue, reservation_url
  `;
  const rows = normalizeDatabaseRows(result);

  return {
    date: normalizeShowDate(rows[0]),
    dates: await getShowDates()
  };
}

async function deleteShowDate(id) {
  const dateId = Number(id);
  if (!Number.isInteger(dateId) || dateId <= 0) {
    return { error: 'Date introuvable.' };
  }

  const db = await getDatabaseClient();
  await ensureShowDatesTable(db);

  const result = await db.sql`
    DELETE FROM show_dates
    WHERE id = ${dateId}
    RETURNING id
  `;
  const rows = normalizeDatabaseRows(result);

  if (!rows[0]) {
    return { error: 'Date introuvable.' };
  }

  return {
    deletedId: String(dateId),
    dates: await getShowDates()
  };
}

module.exports = {
  addShowDate,
  deleteShowDate,
  getShowDates
};
